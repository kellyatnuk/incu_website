    <h2>About the Centre</h2>
    <p>The JNEC Entrepreneurship and Innovation Centre (JNEC-EIC) is set up to provide business support and technology transfer initiatives for various sectors in the multidisciplinary areas of science, technology, and humanities in the region. The establishment of the centre is supported by the Department of Employment and Entrepreneurship under the Ministry of Labor and Human Resources through the Start-up/CSI Flagship program in 2020.</p>
    <p>The main aim of JNEC-EIC is to produce successful firms that will become financially viable and independent through Science and Technology interventions. The centre aims to incubate and materialize conceptual ideas among the students, faculty members of Jigme Namgyel Engineering College, and other relevant institutions/firms. It seeks to transform these ideas into value-added products in the commercial market.</p>
    <p>JNEC-EIC nurtures budding ventures by providing workspace and shared facilities along with focused counseling. The centre offers value-added support, access to critical tools, information, education, networking resources, and prototype funding in the form of research and development that may otherwise have been unaffordable.</p>
